const languageMap = {
    en: 'en',
    zh: 'zh-CN',
    es: 'es',
    ja: 'ja',
    ko: 'ko'
};

function getTranslation(language, key) {
    return key.split('.').reduce((value, part) => value && value[part], translations[language]);
}

function getSavedLanguage() {
    try {
        return localStorage.getItem('athlimate-language');
    } catch (error) {
        return null;
    }
}

function saveLanguage(language) {
    try {
        localStorage.setItem('athlimate-language', language);
    } catch (error) {
        // Language switching should still work even when storage is unavailable.
    }
}

function applyLanguage(language) {
    const selectedLanguage = translations[language] ? language : 'en';
    const metaDescription = document.querySelector('meta[name="description"]');

    document.documentElement.lang = languageMap[selectedLanguage];
    document.title = translations[selectedLanguage].meta.title;

    if (metaDescription) {
        metaDescription.setAttribute('content', translations[selectedLanguage].meta.description);
    }

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const translatedText = getTranslation(selectedLanguage, element.dataset.i18n);

        if (translatedText) {
            element.textContent = translatedText;
        }
    });

    document.querySelectorAll('[data-i18n-alt]').forEach(element => {
        const translatedText = getTranslation(selectedLanguage, element.dataset.i18nAlt);

        if (translatedText) {
            element.setAttribute('alt', translatedText);
        }
    });

    saveLanguage(selectedLanguage);
}

document.addEventListener('DOMContentLoaded', () => {
    const navbar = document.getElementById('navbar');
    const mobileMenu = document.getElementById('mobile-menu');
    const navLinks = document.querySelector('.nav-links');
    const languageSelect = document.getElementById('language-select');

    if (languageSelect) {
        const savedLanguage = getSavedLanguage() || 'en';
        const updateLanguage = () => applyLanguage(languageSelect.value);

        languageSelect.value = translations[savedLanguage] ? savedLanguage : 'en';
        applyLanguage(languageSelect.value);

        languageSelect.addEventListener('change', updateLanguage);
        languageSelect.addEventListener('input', updateLanguage);
    }

    // Navbar scroll effect
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.style.background = 'rgba(255, 255, 255, 0.95)';
            navbar.style.boxShadow = '0 2px 20px rgba(0,0,0,0.1)';
        } else {
            navbar.style.background = 'rgba(255, 255, 255, 0.8)';
            navbar.style.boxShadow = 'none';
        }
    });

    // Mobile menu toggle (Basic implementation)
    if (mobileMenu) {
        mobileMenu.addEventListener('click', () => {
            navLinks.classList.toggle('active');
            mobileMenu.classList.toggle('is-active');
        });
    }

    // Smooth scroll for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                const navHeight = navbar.offsetHeight;
                const targetPosition = targetElement.getBoundingClientRect().top + window.pageYOffset - navHeight;

                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });

    // Simple Intersection Observer for scroll animations
    const observerOptions = {
        threshold: 0.1
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
            }
        });
    }, observerOptions);

    document.querySelectorAll('.feature-card, .science-content').forEach(el => {
        observer.observe(el);
    });
});
